function removeDuplicates(text) {
    let words = text.split(/\s+/);
    let result = [];
    
    for (let i = 0; i < words.length; i++) {
        
        if (i === 0 || words[i] !== words[i - 1]) {
            result.push(words[i]);
        }
    }
    
    return result.join(' ');
}

